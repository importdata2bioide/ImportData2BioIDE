package org.big.entityVO;

public class Species {
	
	private String namedPerson;
	private String namedDate;
	private String taxaID;
	public String getNamedPerson() {
		return namedPerson;
	}
	public void setNamedPerson(String namedPerson) {
		this.namedPerson = namedPerson;
	}
	public String getNamedDate() {
		return namedDate;
	}
	public void setNamedDate(String namedDate) {
		this.namedDate = namedDate;
	}
	public String getTaxaID() {
		return taxaID;
	}
	public void setTaxaID(String taxaID) {
		this.taxaID = taxaID;
	}
	
	

}
